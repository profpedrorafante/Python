# Crie um programa para converter uma temperatura da escala Celsius para a escala Fahrenheit.
# A fórmula para conversão é Fahrenheit = ((Celsius * 9) / 5 ) + 32

celsius = float(input('Informe o valor em Celsius para fazermos a conversão para Fahrenheit: '))
fahrenheit = ((celsius * 9) / 5 ) + 32

print('======== Conversão Celsius -> Fahrenheit\n', fahrenheit)

